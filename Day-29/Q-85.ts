//This function determines the position of the first occurrence of "code" in the given string
function indexofcode(str: string) {
    // Searches for "code" and returns its position
    return str.indexOf("code");
  }
  
  // Test the function with a sample sentence
  console.log(indexofcode("Hello I am a coder; my name is Shiraz Ali")); // output: 11
  